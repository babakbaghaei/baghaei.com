function openProjectModal(index) {
    const project = PROJECTS_DATA[index];
    if (!project) {
        console.error('Project not found at index:', index);
        return;
    }

    const modal = document.getElementById('project-modal');
    const modalContent = document.getElementById('project-modal-content-wrapper');
    const title = document.getElementById('project-modal-title');
    const content = document.getElementById('project-modal-content');

    if (!modal || !modalContent || !title || !content) {
        console.error('Modal elements not found!');
        return;
    }

    title.textContent = project.title;

    // Enhanced content with more details
    content.innerHTML = `
        <div class="mb-6">
            <span class="inline-block px-4 py-2 bg-gray-100 border-2 border-gray-200 text-xs font-black text-gray-600 font-sans">${project.year}</span>
        </div>
        <div class="space-y-6">
            <div>
                <h4 class="text-lg font-black font-display mb-4 text-black">داستان پروژه</h4>
                ${project.content}
            </div>
            <div class="pt-6 border-t-2 border-gray-200">
                <h4 class="text-lg font-black font-display mb-4 text-black">نتیجه و دستاوردها</h4>
                <p class="text-sm text-gray-700 font-sans leading-relaxed mb-4">این پروژه با موفقیت به پایان رسید و نتایج مثبتی برای مشتری به همراه داشت. ما از طریق طراحی دقیق، برنامه‌نویسی حرفه‌ای و توجه به جزئیات، توانستیم محصولی خلق کنیم که هم از نظر عملکرد و هم از نظر تجربه کاربری، رضایت کامل مشتری را جلب کرد.</p>
                <p class="text-sm text-gray-700 font-sans leading-relaxed">این پروژه نمونه‌ای از تعهد ما به کیفیت و نوآوری است و نشان می‌دهد که چگونه می‌توان با ترکیب خلاقیت و تکنولوژی، محصولاتی خلق کرد که واقعاً تفاوت ایجاد می‌کنند.</p>
            </div>
        </div>
    `;

    modal.classList.remove('hidden');
    modal.classList.add('flex');
    document.body.style.overflow = 'hidden';

    modalContent.style.transition = 'none';
    modalContent.style.transform = 'scale(0.9)';
    modalContent.style.opacity = '0';

    void modalContent.offsetWidth;

    requestAnimationFrame(() => {
        requestAnimationFrame(() => {
            modalContent.style.transition = 'transform 0.3s cubic-bezier(0.4, 0, 0.2, 1), opacity 0.3s ease-out';
            modalContent.style.transform = 'scale(1)';
            modalContent.style.opacity = '1';
        });
    });
}

function closeProjectModal() {
    const modal = document.getElementById('project-modal');
    const modalContent = document.getElementById('project-modal-content-wrapper');

    if (!modal || !modalContent) return;

    modalContent.style.transition = 'transform 0.2s ease-in, opacity 0.2s ease-in';
    modalContent.style.transform = 'scale(0.95)';
    modalContent.style.opacity = '0';

    setTimeout(() => {
        modal.classList.add('hidden');
        modal.classList.remove('flex');
        document.body.style.overflow = '';
        modalContent.style.transform = 'scale(0.9)';
        modalContent.style.opacity = '0';
    }, 200);
}

// Make functions globally available
window.openProjectModal = openProjectModal;
window.closeProjectModal = closeProjectModal;
